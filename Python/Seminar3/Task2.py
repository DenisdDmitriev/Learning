# Дана последовательность из N целых чисел и число
# K. Необходимо сдвинуть всю последовательность
# (сдвиг - циклический) на K элементов вправо, K –
# положительное число.
# Input: [1, 2, 3, 4, 5] k = 3
# Output: [4, 5, 1, 2, 3]

my_list = [1, 2, 3, 4, 5]
k = 2
for i in range(k):
    last_elem = my_list.pop()
    my_list.insert(0, last_elem)
print(my_list)
